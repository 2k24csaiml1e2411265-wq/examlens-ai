from reportlab.lib.pagesizes import A4
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import cm
from reportlab.lib import colors
from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle, HRFlowable
from reportlab.lib.enums import TA_CENTER, TA_LEFT, TA_JUSTIFY
import io


def _base_doc(buffer):
    return SimpleDocTemplate(
        buffer, pagesize=A4,
        rightMargin=2*cm, leftMargin=2*cm,
        topMargin=2*cm, bottomMargin=2*cm
    )


def generate_predictions_pdf(predictions_text: str, subject: str) -> bytes:
    buffer = io.BytesIO()
    doc = _base_doc(buffer)
    styles = getSampleStyleSheet()

    title_style = ParagraphStyle("title", parent=styles["Title"],
        fontSize=18, textColor=colors.HexColor("#5a4fcf"),
        spaceAfter=6, alignment=TA_CENTER)
    sub_style = ParagraphStyle("sub", parent=styles["Normal"],
        fontSize=10, textColor=colors.grey, alignment=TA_CENTER, spaceAfter=20)
    h2_style = ParagraphStyle("h2", parent=styles["Heading2"],
        fontSize=13, textColor=colors.HexColor("#5a4fcf"), spaceBefore=14, spaceAfter=6)
    body_style = ParagraphStyle("body", parent=styles["Normal"],
        fontSize=10, leading=16, spaceAfter=4)

    story = [
        Paragraph("ExamLens AI — Predicted Questions", title_style),
        Paragraph(f"Subject: {subject} | Generated by ExamLens AI", sub_style),
        HRFlowable(width="100%", thickness=1, color=colors.HexColor("#5a4fcf")),
        Spacer(1, 12),
    ]

    for line in predictions_text.split("\n"):
        line = line.strip()
        if not line:
            story.append(Spacer(1, 6))
        elif line.startswith("## ") or line.startswith("# "):
            story.append(Paragraph(line.replace("#", "").strip(), h2_style))
        else:
            clean = line.replace("**", "").replace("*", "")
            story.append(Paragraph(clean, body_style))

    doc.build(story)
    return buffer.getvalue()


